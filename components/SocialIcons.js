// Helper: renders a row of social icon links for a profile
export function SocialLinks({ profile, size = 28 }) {
  if (!profile) return null;
  const links = [
    { key: "website",   url: profile.website,   Icon: WebsiteIcon,   color: "#701890" },
    { key: "instagram", url: profile.instagram,  Icon: InstagramIcon, color: "#E1306C" },
    { key: "facebook",  url: profile.facebook,   Icon: FacebookIcon,  color: "#1877F2" },
    { key: "tiktok",    url: profile.tiktok,     Icon: TikTokIcon,    color: "#000000" },
    { key: "youtube",   url: profile.youtube,    Icon: YouTubeIcon,   color: "#FF0000" },
    { key: "x_twitter", url: profile.x_twitter,  Icon: XIcon,         color: "#000000" },
  ].filter(l => l.url && l.url.trim());

  if (links.length === 0) return null;
  return (
    <div style={{ display: "flex", gap: 12, flexWrap: "wrap", alignItems: "center" }}>
      {links.map(({ key, url, Icon, color }) => (
        <a key={key} href={url.startsWith("http") ? url : `https://${url}`} target="_blank" rel="noreferrer"
          style={{ display: "inline-flex", opacity: 1, transition: "opacity 0.2s" }}
          onMouseEnter={e => e.currentTarget.style.opacity = "0.7"}
          onMouseLeave={e => e.currentTarget.style.opacity = "1"}>
          <Icon size={size} color={color} />
        </a>
      ))}
    </div>
  );
}
